       <p style="font-size: 15px;padding: 5px;"> 


    Alam Medical College, the Hospital and the Nursing Training Institute have been established under the Aftab–Rahima Welfare Trust as a conglomerate offering medical education & research, hospital care as well as training in nursing services. The complex is situated in Bhagalpur village of Bajitpur thana of Kishoregonj district. The great philanthropist of this area Late Mr. Jahurul Islam is the founder of these organizations in his native land.

Bhagalpur – Bajitpur is about 110 km north – east of Dhaka city. It is connected by Egaro Shindur Express train and has excellent road connection with the Capital. In either way, Bhagalpur can be reached within three hours only.

In the first phase in 1989, a 250 bed hospital was put into service. It is equipped with most modern instruments and facilities, offering round the clock services including emergencies, for indoor as well as outdoor patients. At present there are more than 500 beds covering all the departments of a general tertiary level teaching hospital. In its final and ultimate phase of completion, the Hospital will have more beds and specialized departments to provide management and services of all aspects of medical, surgical or emergency requirements.



The College at present is entirely self contained with own buildings housing all departments, laboratories, library, museums, dissection hall, lecture theaters, cafeteria, hostels for students of either sex, accommodation facilities for teachers etc. Indeed, the idyll serenity of the campus and the beautifully landscaped terrain provide a pollution free congenial environment for modern method of teaching and learning. The College is affiliated and approved institution of Dhaka University, Bangladesh Medical & Dental Council (BMDC) and recognized by World Health Organization (WHO).








